<template>
  <div :class="$style.tabbedWindow">
    <div :class="$style.tabsContainer">
      <div ref="tabWrapper">
        <div
          :class="[$style.tabWrapper, isActiveTab(i)]"
          :id="`${idPrefix}_${i}`"
          v-for="(tab, i) in tabs"
          :key="i"
          :style="tabStyle(i)"
        >
          <svg>
            <polygon points="0,20 60,20 70,0 10,0" fill="none" stroke="white" stroke-width="1">
            </polygon>
            <foreignObject x="20" y="2" width="40" height="20">
              <p @click="toggleTab(i)" :id="tab.title">
            {{ tab.title }}
              </p>
            </foreignObject>
          </svg>
          
        </div>
      </div>
    </div>

    <div :class="$style.panelsContainer">
      <div
        :class="[$style.panelWrapper, isActiveTab(i)]"
        v-for="(tab, i) in tabs"
        :key="i"
      >
        <div :class="$style.tabContent">
          <slot :name="tab.name"></slot>
        </div>
      </div>
    </div>
  </div>
</template>

<script>
export default {
  props: {
    tabs: {
      required: true,
      type: Array,
    },
    idPrefix: {
      required: false,
      type: String,
      default: "tab",
    },
  },
  data() {
    return {
      currentTab: 0,
    };
  },
  computed: {
    isActiveTab() {
      return (index) => (index === this.currentTab ? this.$style.active : "");
    },
  },
  methods: {
    toggleTab: function(index) {
      if (this.tabs.length * 130 > window.innerWidth) this.alignTab(index);
      this.currentTab = index;
      this.$emit('onToggleTab', this.currentTab); 
    },
    tabStyle: function(index) {
      return {
        left: `${index * 100}px`,
      };
    },
    alignTab: function(index) {
      const tab = document.getElementById(`${this.idPrefix}_${index}`);
      const tabWidth = tab.offsetWidth;
      let xDiff = (index - this.currentTab) * (window.innerWidth * 0.2);
      if (xDiff < 0) xDiff = 0;
      this.$refs.tabWrapper.style.transform = `translateX(${-xDiff}px)`;
    },
  },
};
</script>

